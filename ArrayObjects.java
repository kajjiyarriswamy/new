package com.technoelevate.implementation_of_array;

public class ArrayObjects {
	String name;
	int id;
	public ArrayObjects(String name, int id) {
		
		this.name = name;
		this.id = id;
	}
	@Override
	public String toString() {
		return "ArrayObjects [name=" + name + ", id=" + id + "]";
	}
	
	
	

}
